num = int(input("Digite um número correspondete ao dia da semana (ex: 1-domingo, 2-segunda...): "))
if num == 1:
    print("É domingo")
elif num == 2:
    print("É segunda")
elif num == 3:
    print("É terça")
elif num == 4:
    print("É quarta")
elif num == 5:
    print("É quinta")
elif num == 6:
    print("É sexta")
elif num == 7:
    print("É sabado")
else:
    print("Número invalido")